 function executaWhile(){
    let num = (document.getElementById("entrada").value)
    let i = 0;
    while(i < num){
        alert(i);
        i ++;
    }
 }
 function executaFor(){
    let num = (document.getElementById("entrada").value)
    for(let i = 0; i < num; i++){
        alert(i);
    }
 }
